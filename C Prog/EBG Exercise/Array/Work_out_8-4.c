#include <stdio.h>
#include <conio.h>

int main()
{
    int num_row, num_col, cell;
    num_row = 10;
    num_col = 12;
    int i, j;
    printf("\nPrinting multiplication table : ");
    printf("\n-------------------------------\n");
    printf("\n.-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------.");
    /*printf("\n|           |    ");*/
    printf("\n|   Table   |    ");
    for (i = 1; i <= num_col; i++)
    {
        printf("%*d    |    ", 3, i);
    }
    for (i = 1; i <= num_row; i++)
    {
        printf("\n|-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------|");
        printf("\n|    %*d    |    ", 3, i);
        for (j = 1; j <= num_col; j++)
        {
            cell = i * j;
            printf("%*d    |    ", 3, cell);
        }
    }
    printf("\n'-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------+-----------'");
    printf("\n\nPress any key to exit....");
    getch();
    printf("\n\n");
    return 0;
}